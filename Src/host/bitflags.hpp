//установить флаги
#define b_set(a,b) ([a,b] call bit_set)

//определить фгали
#define b_def(bits) [0,bits] call bit_set

//проверить есть ли флаг
#define b_check(a,b) ([a,b] call bit_check)

//все флаги в массив
#define b_toArray(bits) ((bits) call bit_toArray)


// Побитовые операции

//побитовое не ~11001010
//				00110101
#define b_not(bin) ((bin) call bitwise_not)

/*
	Побитовое И:Побитовое И используется для выключения битов. 
	Любой бит, установленный в 0, вызывает установку соответствующего бита результата также в 0.
	
	11001010
&	11100010
	--------
	11000010
*/
#define b_and(a,b) ([a,b] call bitwise_and)

/*
	Побитовое ИЛИ: Побитовое ИЛИ используется для включения битов. 
	Любой бит, установленный в 1, вызывает установку соответствующего бита результата также в 1.
	
	11001010
|	11100010
	--------
	11101010
*/

#define b_or(a,b) ([a,b] call bitwise_or)

/*
	Побитовое исключающее ИЛИ:
	Исключающее ИЛИ устанавливает значение бита результата в 1, 
	если значения в соответствующих битах исходных переменных различны.
	
	11001010
^	11100010
	--------
	00101000
*/

#define b_xor(a,b) ([a,b] call bitwise_xor)

